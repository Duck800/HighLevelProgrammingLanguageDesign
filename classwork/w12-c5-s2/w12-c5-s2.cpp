#include <iostream>
#include <iomanip>
using namespace std;

void count(int yong[14][14],int row)
{
	int i, j;
	for (i = row - 1; i >= 0; i--) {
		for (j = row - 1; j >= 0; j--) {
			if (j == row - 1 || i == j)
				yong[i][j] = 1;
			else
				yong[i][j] = yong[i + 1][j] + yong[i + 1][j + 1];
		}
	}

}

int main()
{
	int row, i, j;
	int yong[14][14] = { 0 };
	cout << "请输入要打印的行数[3..13]" << endl;
	cin >> row;
	count(yong, row);
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	for (i = 0; i < row; i++) {
		for (j = 0; j < row; j++) {
			if (yong[i][j] != 0)
				cout << setw(6) << yong[i][j];
			else
				cout << setw(6) << " ";
		}
		cout << endl;
	}
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << endl;
}