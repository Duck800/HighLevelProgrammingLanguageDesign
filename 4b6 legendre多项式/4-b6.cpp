   /***************************************************************************
	 函数名称：
	 功    能：返回n阶legendre多项式的值
	 输入参数：
	 返 回 值：
	 说    明：函数名、形参、返回类型均不准动
   ***************************************************************************/
double legendre(double x, int n)
{
	double i = 0;
	if (n == 0)
		i = 1;
	else if (n == 1)
		i = x;
	else if (n > 1)
		i = ((2*(double)n - 1) * x * legendre(x, n - 1) - ((double)n - 1) * legendre(x, n - 2)) / n;
	return i;
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：完成输入、调用递归函数、输出
***************************************************************************/
int main()
{
	cout << "计算legendre，请输入x和n的值" << endl; 
	double x;
	int n;
	cin >> x >> n;
	double i = legendre(x, n);
	cout << "legendre[" << n << "](" << x << ")=" << i << endl;;
	return 0;
}
